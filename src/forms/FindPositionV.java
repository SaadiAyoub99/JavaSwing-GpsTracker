    private void searchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_searchActionPerformed

          Date dateDebut = DateDebuttxt2.getDate();
        Date dateFin      = DateFintxt2.getDate();

        // Tracker tracker  = (Tracker) listeTrackers2.getSelectedItem();
        Vehicule vehicule = (Vehicule) Vehiculetxt1.getSelectedItem();
        DateFormat df = new SimpleDateFormat("yyyy-MM-dd");
        String dateD = df.format(dateDebut);
        String dateF = df.format(dateFin);

        model.setRowCount(0);

        for (Position p : vts.PosVecBetweenDate(vehicule, dateD, dateF) ) {
            model.addRow(new Object[]{
                p.getId(),
                p.getLatitude(),
                p.getLongitude(),
                p.getTracker()
            });
        }
    }//GEN-LAST:event_searchActionPerformed


