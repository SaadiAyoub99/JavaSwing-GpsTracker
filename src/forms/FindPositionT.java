    private void searchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_searchActionPerformed

        Date dateDebut = DateDebuttxt2.getDate();
        Date dateFin = DateFintxt2.getDate();

        Tracker tracker = (Tracker) listeTrackers2.getSelectedItem();

        DateFormat df = new SimpleDateFormat("yyyy-MM-dd");
        String dateD = df.format(dateDebut);
        String dateF = df.format(dateFin);

        model.setRowCount(0);

        try {
            for (Position p : vts.findPositionsBetweenDates(tracker, dateD, dateF)) {
                model.addRow(new Object[]{
                    p.getId(),
                    p.getLatitude(),
                    p.getLongitude(),
                    p.getTracker()
                });
            }
        } catch (ParseException ex) {
            Logger.getLogger(FindPositionT.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_searchActionPerformed

